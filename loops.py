# Example of for loop using a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Output:
# apple
# banana
# cherry
names = ["Kipngenoh","Exalted","Rotich","Itembe"]
for name in names:
        print(name)

# Output:
# Kipngenoh
# Exalted
# Rotich
# Itembe

# Example using a while loop
count = 0
while count < 5:
    print("Count is:", count)
    count += 1

# Output:
# Count is: 0
# Count is: 1
# Count is: 2
# Count is: 3
# Count is: 4

#Nested loop
for i in range(3):
    for j in range(2):
        print(i, j)

# Output:
# 0 0
# 0 1
# 1 0
# 1 1
# 2 0
# 2 1
